//Incrementos y Decrementos

let puntaje = 5;
console.log(puntaje);

console.log(++puntaje);
console.log(puntaje);

puntaje -= 3;
console.log(puntaje);
//Incremento: Llamo a la variable, la incremento y la vuelvo a llamar para imprimir. puntaje++
//Pre-incremento: Incrementa al momento de llamarse para imprimir. ++puntaje

//Decremento: Llamo a la variable, la decremento y la vuelvo a llamar para imprimir. puntaje--
//Pre-decremento: Decrementa al momento de llamarse para imprimir. --puntaje
